export function getBalancedQuestions(e = 20) {
  const t = [
      ...improvedQuestions.openness,
      ...improvedQuestions.conscientiousness,
      ...improvedQuestions.extraversion,
      ...improvedQuestions.agreeableness,
      ...improvedQuestions.neuroticism,
      ...improvedQuestions.behavioral,
      ...improvedQuestions.situational,
      ...improvedQuestions.preferences
    ],
    r = [];
  ['Openness', 'Conscientiousness', 'Extraversion', 'Agreeableness', 'Neuroticism'].forEach(s => {
    const o = t.filter(e => e.category === s).sort(() => Math.random() - 0.5);
    r.push(...o.slice(0, Math.min(2, Math.floor(e / 5))));
  });
  const s = e - r.length,
    o = t.filter(e => !r.includes(e)).sort(() => Math.random() - 0.5);
  return (r.push(...o.slice(0, s)), r.sort(() => Math.random() - 0.5));
}
